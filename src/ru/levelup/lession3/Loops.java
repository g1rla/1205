package ru.levelup.lession3;

public class Loops {
    public static void main(String[] args) {
        // i, j, k
        for (int counter = 0; counter < 10; counter++) {
            // counter++ : counter = counter + 1
            // counter-- : counter = counter - 1
            System.out.println("Hello Nelly " + counter);
        }
    }
}
